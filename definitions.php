    <p> <span class="Mot" >  Inflation :</span> <span class="definition"  > Déséquilibre économique se manifestant par une hausse durable du niveau général des prix </span></p>
    <p> <span class="Mot" >  Désinflation :</span> <span class="definition"  > Ralentissement de l'inflation. Le taux d'inflation diminue, sans devenir nul ou négatif. La hausse des prix continue, mais à un moindre rythme. </span></p>
    <p><span  class="Mot">  Dette publique :</span> <span class="definition"  > Ensemble des emprunts effectués par l'Etat. </span></p>
    <p><span class="Mot">  Chomâge :</span> <span class="definition"  > Situation ou une partie de la population d'un pays en age de travailler qui se trouve sans emploi et à la recherche d'un emploi ; les chômeurs sont inclus dans la population active. </span></p>
    <p><span class="Mot">  IDH :</span> <span class="definition"  > Indicateur de développement humain il vise à évaluer le niveau de développement des pays en se fondant non pas sur des données strictement économiques, mais sur la qualité de vie de leurs ressortissants. </span></p>
    <p><span  class="Mot">  PIB :</span> <span class="definition"  >  Produit Interieur Brut, il vise à mesurer la richesse créée par tous les agents, privés et publics, sur un territoire national pendant une période donnée</span></p>
    <p><span  class="Mot">  Déficit :</span> <span class="definition"  > Situation dans laquelle les recettes du budget de l'Etat sont inférieures aux dépenses ; à noter que depuis 2012 le pacte européen signé par 25 pays de l'UE impose la régle d'or budgétaire : les pays s'engagent à avoir des budgets en équilibre ou en excédent Lorsqu'un déficit se répète, l'État doit emprunter pour le financer, ce qui augmente la dette publique, c'est-à-dire l'ensemble des sommes dues par l'État à ses créanciers. </span></p>
    <p><span class="Mot">  Taux d'intérêt directeur :</span> <span class="definition"  > Taux fixé par la banque centrale pour influencer le coût des crédits dans l'économie.</span></p>    
    <p><span class="Mot">  Correlation :</span><span class="definition"  > Variation simultanée de deux variables dans le même sens (corrélation positive) ou en sens opposé (corrélation négative), elle est parfois appelée covariance.Cependant, la corrélation entre deux variables, telle qu'on peut l'observer sur un graphique, ne signifie pas forcément qu'il existe un lien de cause à effet. Deux variables peuvent varier ensemble sans qu'une ne cause directement l'autre ; il peut y avoir un facteur tiers ou une simple coïncidence. </span></p>
